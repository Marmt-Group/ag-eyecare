                        <h2>Different types Refractive errors</h2>
                        <h3>Myopia</h3>
                        <p>Myopia or nearsightedness, is a type of refractive error where light is focused in front of the retina. It is the in-ability to see objects at distance clearly. This is the most common refractive error. If myopia is untreated, it can cause squinting, eyestrain, fatigue and headaches. Myopia manifests when the eye grows too long. It usually starts in early childhood and stabilizes in adulthood. Myopia is treated with glasses, contact lenses, or LASIK surgery.</p>
                        <h3>Hyperopia</h3>
                        <p>Hyperopia or farsightedness, is a type of refractive error where light is focused behind the retina. People notice objects at near are blurry while objects at distance are clear. Common signs and symptoms of hyperopia include headaches, eyestrain, squinting, or blurred vision at near. Hyperopia is commonly missed in vision screenings, but can be discovered in a comprehensive eye exam. It can be corrected with glasses, contact lenses or LASIK.</p>
                        <h3>Presbyopia</h3>
                        <p>Presbyopia is a common vision disorder that occurs around the age of 40. It is an age related condition and occurs because the lens of the eye becomes thicker and loses flexibility to focus and accommodate. Presbyopic eyes lose the ability to see objects at near. The condition can be corrected with readers or prescription eyeglasses as well as multifocal or monovision contact lenses. Presbyopia is rarely corrected with LASIK surgery.</p>
                        <h3>Astigmatism</h3>
                        <p>Astigmatism is a type of refractive error where light is focused on multiple points of the retina. Because there are several focal points, vision is blurred at near and distance. Astigmatism commonly causes eyestrain after prolonged visual activities. It can be caused by an irregularly shaped cornea or an irregularity within the lens behind your eye. Astigmatism can be corrected through glasses or contact lenses.</p>
