                        <h3>Glaucoma</h3>
                        <p>Glaucoma is a chronic and degenerative disease that damages the optic nerve, resulting in vision loss and blindness. High ocular pressure is closely related to the extent of damage on the optic nerve. Glaucoma causes you to lose peripheral vision and if it goes undiagnosed, it will eventually affect your central vision. Because glaucoma is a slowly progressing disease, there are no visual symptoms. It usually takes 1-3 years of consistent IOP measuring and visual field testing to diagnose a patient. Glaucoma cannot be cured but can be managed with IOP lowering drops or with surgery.</p>
                        <h3>Cataracts</h3>
                        <p>Cataracts are commonly an age related disease that affects people over the age of 50. Cataracts are a clouding of the intraocular lens that causes blurred vision and an increase in glare. This is the leading cause of blindness around the world.  The types of cataracts include nuclear cataracts (the most common), which develops in the center of the lens, cortical cataracts develop and grow like spokes on a wheel, and posterior subcapsular cataracts develop in the back of the lens. Cataracts can be treated with surgery.</p>
                        <h3>Macular Degeneration</h3>
                        <p>Age related macular degeneration is a disease that causes vision loss in the central vision. It damages the macula which is the part of the eye that allows you to see small sharp details in your central vision. Macular degeneration can develop slowly and cause trace to moderate decreased vision. As time goes by the blurred spot can get bigger and colors will appear less vibrant and objects will be less bright. Blindness can happen with severe and aggressive cases of macular degeneration and must be treated with ocular injections. Age related macular degeneration cannot be cured but can be prevented and minimized by not smoking and taking Lutein vitamins.</p>
                        <h3>Diabetic Retinopathy</h3>
                        <p>Diabetic Retinopathy is a condition that causes damage to blood vessels in the back of your eye and occurs when the blood sugar is not controlled. This disease is seen in people who have too much sugar in their blood. Diabetic retinopathy is first seen when small blood vessels leak blood, then later leak additional fluids. The leakage will cause the retina to become inflamed and will cause vision to fluctuate in and out of focus. Diabetic retinopathy treatment usually includes lowering blood sugar. Severe cases will require a retinal laser.</p>
                        <h3>Retinal holes/tears</h3>
                        <p>Retinal holes and tears happen when layers of the retina begin to weaken or are stretched thin. There are usually no symptoms associated with holes and tears but some will report large colorful flashes of light followed by a new onset of floaters. Others have reported a dark curtain or shadow looming in the periphery. Holes and tears are commonly seen in people with a high near sighted prescription, people with ocular trauma and/or people who recently had eye surgery. Holes and tears are treatable with numerous retinal procedures that can involve retinal lasers or freezing tissue.</p>
